//Qualquer divisão por 0 retorna Infinity
console.log(7 / 0) //Output: Infinity

//Se o dado String for equivalente a um Number, operações podem ser realizadas com o mesmo.
console.log(10 / "2") //Output: 5

//Operações com Strings não funcionam ;-;
console.log('Show' * 2) //Output: NaN

//Operações com o uso de valores com ponto flutuante não são 100% precisas ;-;
console.log(0.7 + 0.1) //Output: 0.7999999999999999
