print("Digite o valor da base maior:")
a = float(input())
print("Digite o valor da base menor:")
b = float(input())
print("Digite o valor da altura:")
c = float(input())

# a) area do trapézio
area_trap = ((a + b) * c) / 2
print("A área do trapézio é:",area_trap)

# b) área do quadrado
area_quad = b * b
print("A área do quadrado é:", area_quad)

# c) área de superficie de um cubo
area_cubo = 6 * c * c
print("área do cubo:", area_cubo)
